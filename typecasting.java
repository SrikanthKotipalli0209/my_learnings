public class typecasting {

    public static void main(String[] args)
    {
        int i = 100;
        byte b= (byte)i;

        byte c = 2;
        int e = (int)c;

        float f = 2.5f;
        double d = (double)f;

        double g = 9.478;
        float k = (float)g;

        char s = '8';
        int n = (int)s;

        int u = 98;
        char j = (char)u;


        
        System.out.println(b);
        System.out.println(e);
        System.out.println(d);
        System.out.println(k);
        System.out.println(n);
        System.out.println(j);





    }
    
}
